package cn.niit.jdbc.domain;

public class EducationExperience {
	
	private int educationID;
	private String schoolName;
	private String majorStudied;
	private String admissionTime;
	private String Education;
	private String relatedSkils;
	private String achievements;
	private String name;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getEducationID() {
		return educationID;
	}
	public void setEducationID(int educationID) {
		this.educationID = educationID;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getMajorStudied() {
		return majorStudied;
	}
	public void setMajorStudied(String majorStudied) {
		this.majorStudied = majorStudied;
	}
	public String getAdmissionTime() {
		return admissionTime;
	}
	public void setAdmissionTime(String admissionTime) {
		this.admissionTime = admissionTime;
	}
	public String getEducation() {
		return Education;
	}
	public void setEducation(String education) {
		Education = education;
	}
	public String getRelatedSkils() {
		return relatedSkils;
	}
	public void setRelatedSkils(String relatedSkils) {
		this.relatedSkils = relatedSkils;
	}
	public String getAchievements() {
		return achievements;
	}
	public void setAchievements(String achievements) {
		this.achievements = achievements;
	}
	
	

}
